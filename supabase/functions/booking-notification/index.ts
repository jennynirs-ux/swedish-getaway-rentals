const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    );

    const { record } = await req.json();
    
    console.log('New booking notification:', record);

    // Here you could integrate with external notification services
    // For example: email notifications, SMS, Slack, etc.
    
    // Example: Send email to admin (placeholder)
    if (record.status === 'pending') {
      console.log(`New booking received: ${record.guest_name} for property ${record.property_id}`);
      
      // You could integrate with services like:
      // - SendGrid for email notifications
      // - Twilio for SMS notifications
      // - Slack webhooks for team notifications
    }

    return new Response(
      JSON.stringify({ success: true }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );

  } catch (error) {
    console.error('Error in booking notification:', error);
    return new Response(
      JSON.stringify({ error: error.message }),
      { 
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' }
      }
    );
  }
});